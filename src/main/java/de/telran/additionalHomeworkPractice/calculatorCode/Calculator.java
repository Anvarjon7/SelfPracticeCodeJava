package de.telran.calculatorCode;

public class Calculator {
    public int sum (int x, int y) {
        int result = x + y;
        return result;
    }
    public int sub (int x, int y) {
        int result = x - y;
        return result;
    }
    public int  mult (int x, int y) {
        int result = x * y;
        return result;
    }
    public int div (int x, int y) {
        int result = x / y;
        return result;
    }
}
